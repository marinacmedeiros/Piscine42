#include <unistd.h>

void	ft_print_comb(void)
{
	char	m;
	char	n;
	char	o;

	m = '0';
	while (m <= '7')
	{
		n = m + 1;
		while (n <= '8')
		{
			o = n + 1;
			while (o <= '9')
			{
				write(1, &m, 1);
				write(1, &n, 1);
				write(1, &o, 1);
				if (m != '7')
					write(1, ", ", 2);
				o++;
			}
			n++;
		}
		m++;
	}
}
